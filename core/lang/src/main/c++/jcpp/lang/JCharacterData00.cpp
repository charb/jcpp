	jbyte JCharacterData00::getDirectionality(jint ch) {
		jint val = getProperties(ch);
		jbyte directionality = (jbyte)((val & 0x78000000) >> 27);

		if (directionality == 0xF ) {
			switch(ch) {
				case 0x202A :
					// This is the only char with LRE
					directionality = JCharacter::DIRECTIONALITY_LEFT_TO_RIGHT_EMBEDDING;
					break;
				case 0x202B :
					// This is the only char with RLE
					directionality = JCharacter::DIRECTIONALITY_RIGHT_TO_LEFT_EMBEDDING;
					break;
				case 0x202C :
					// This is the only char with PDF
					directionality = JCharacter::DIRECTIONALITY_POP_DIRECTIONAL_FORMAT;
					break;
				case 0x202D :
					// This is the only char with LRO
					directionality = JCharacter::DIRECTIONALITY_LEFT_TO_RIGHT_OVERRIDE;
					break;
				case 0x202E :
					// This is the only char with RLO
					directionality = JCharacter::DIRECTIONALITY_RIGHT_TO_LEFT_OVERRIDE;
					break;
				default :
					directionality = JCharacter::DIRECTIONALITY_UNDEFINED;
					break;
			}
		}
		return directionality;
	}

